<h2 class="mHead"><img src="images/mh_meat.gif" alt="2/1 バレンタインはパパに感謝" /></h2>
<div class="columnSection">
<h3 class="sHead">子供と一緒に作るおいしいバレンタイン</h3>
</div>

<div class="lunchSection">
<p class="lunchTxt">2月はいよいよ、フロリダグレープフルーツの旬。<br />
張り切ってスーパーで買いこんでは、白い息を吐きながらせっせと持ち帰ります。<br />
まずはひとつ、ワシワシと豪快に手でむいて娘と半分こして食べます。<br />
グレープフルーツの香りが温かい部屋に広がって、しばらくは自然なアロマ。<br />
お買い物帰りに凍えた体で深呼吸。元気がじんわり湧いてきます。</p>
<div class="sectionLeft">
<p><img src="images/img_lunch.jpg" alt="子供と一緒に作るおいしいバレンタイン" /></p>
<ul class="menuList">
<li class="food">&#9650;・<a href="recipe_07.html">牛肉の赤ワイン煮。グレープフルーツソース</a></li>
<li>・<a href="recipe_02.html">チキンとグレープフルーツのパエリア</a></li>
<li>・<a href="recipe_13.html">グレープフルーツとチョコのタルト</a></li>
</ul>
</div>
<div class="reciepContent">
<p>今年のバレンタインはどうしようか。4年前からバレンタインは娘と一緒に台所に立ちます。そう、娘が2歳の時からの楽しいイベントのひとつ。&quot;好きな男の人に想いを伝える&quot;バレンタインですから、娘も私も最初に浮かぶのは我が家のお父さん。ともに頑張るのです。<br />
今年は&quot;チョコレート&quot;だけでは想いを伝えきれない（？）と、献立も一緒に考えてみました。<br />
メインはじっくり時間をかけて作る<a href="recipe_07.html">赤ワイン煮。</a><br />
コクのあるソースがしっとりホロホロのお肉に絡んで時間をかけただけある自慢の一皿。グレープフルーツのきりっとした香りをあわせると一層上品な味わいになるのです。<br />
二人で味見をしたらすっかりテンションが上がって娘と黙々とグレープフルーツをむきました。<br />
そして主食は、ごはん好きなお父さんのために<a href="recipe_02.html">パエリア</a>で。野菜たっぷりでグレープフルーツの酸味がアクセントのパエリアはサラダ要らず。<br />
野菜をモリモリ取り入れた献立は、お母さんとしても、夫を想う妻としてもちょっと安心します。野菜はすべて娘が切ってくれます。パプリカやアスパラガス、もうすぐ来る春の香り。野菜を切りながら“あすぱらがす　の　によい！　いーによい！！”と言いながら深呼吸。横で私がグレープフルーツを切りはじめたら、一層深く深呼吸しています。一緒に台所に立つと、調理前の食材の新鮮な香りも、綺麗な色も一緒に楽しめるのが嬉しい。<br />
しかも、想う相手は同じく、お父さん。ああ。バレンタインって楽しいなあ。<br />
デザートは<a href="recipe_13.html">チョコレート</a>。タルトの中にも外にもグレープフルーツ。<br />
甘いものは積極的に好きではないお父さんも、グレープフルーツのジューシーな香りと歯触りにきっと喜んでくれるはず。チョコと柑橘系の相性は抜群。チョコはグレープフルーツに上品な甘みとコク深い香りを。グレープフルーツはチョコの甘みに気持ちのよい後味と、まろやかな香りに大人の酸味をプラスしてくれます。ビターなチョコとちょこっとビターなグレープフルーツ。似たもの同志でありながら互いを上手に引き立てあう。<br />
うーん・・・こんなカップルになりたいものです。<br />
ブツブツ考えているうちに、溶かして流しこむだけの簡単タルトは娘が仕上げてくれそうです。</p>
<p>こんな風にグレープフルーツ満載でお料理した日は、夜通しキッチンが、リビングがいい香り。バレンタインが終わっても、またグレープフルーツを娘と抱えて帰ってくるんだろうな私。<br />
今度はどんな献立にしよう。</p>
