<h4>【アイコンの注意事項】</h4>
<ul>
<li>※アイコンは、WIN、MACのみに対応しております。</li>
<li>※アイコンの設定方法についてのお問い合わせは、対応しかねます。</li>
<li>※その他、ダウンロードに関わる障害等については 責任は負いかねます。</li>
</ul>

<h4>・MACでのアイコンの変更について</h4>
<ul>
<li>1.アイコンファイルを選択し、メニューバーの[ファイル]をクリックして、[情報を見る]を選択します。</li>
<li>2.アイコンファイルの情報」画面が開いたら左上のアイコンの画像をクリックします。</li>
<li>3.メニューバーの[編集]から[コピー]を選択します。</li>
<li>4.「アイコンファイルの情報」画面を閉じます。</li>
<li>5.変更する対象のアイコンを選択し、メニューバーの[ファイル]をクリックして、[情報を見る]を選択します。</li>
<li>6.「変更する対象のアイコンの情報」画面が開いたら左上のアイコンの画像をクリックします。</li>
<li>7.メニューバーの[編集]から[ペースト]を選択し、アイコンを変更します。</li>
</ul>


<h4>・WINでのアイコンの変更について</h4>
<ul>
<li>1.変更対象のアイコンの上で右クリックし、「プロパティ」を選択します。</li>
<li>2. [カスタマイズ]タブを選択して、[アイコンの変更]ボタンをクリックします。</li>
<li>3. [参照]ボタンをクリックして、アイコンファイルを選択して、[OK]ボタンをクリックします。</li>
<li>4. プロパティの画面の[OK]ボタンをクリックし、アイコンを変更します。</li>
</ul>

