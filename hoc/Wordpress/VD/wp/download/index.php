<h4>【Windowsをご利用の方】</h4>
<p class="osTxt">表示された壁紙画像の上でマウスの右ボタンをクリックし、メニューから「壁紙に設定」または「背景に設定」を選択してください。</p>

<h4>【Macintoshをご利用の方】</h4>
<p class="osTxt">表示された壁紙画像の上でcontrol+クリックし、ハードディスクに保存します。「システム環境設定」→「デスクトップとスクリーンセーバー」を選択します。現在のデスクトップピクチャを表示したサムネイル画面に保存した画像ファイルのアイコンをドラッグします。</p>
<ul class="list">
<li>※上記の方法で壁紙を設定すると、現在ご利用されている壁紙が置き換えられてしまいますのでご注意ください。</li>
<li>※元の壁紙に戻すには、変更前の壁紙の保存先・ファイル名を忘れないようにしてください。</li>
<li>※お使いのOSのバージョンによって設定方法が異なる場合があります。</li>
</ul>
