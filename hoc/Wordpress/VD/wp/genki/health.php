<h3><img src="images/health/sh_influenza.gif" alt="インフルエンザ対策にも。" /></h3>
<div class="content">
<p>かぜやがん対策に効果が期待されるビタミンCが、1/2個で一日に必要な量を摂取できます。ビタミンCは、肌にうるおいを与え、疲労、ストレスからの回復も助けます。</p>
<!-- / class content --></div>
<!-- / class sectionContent --></div>

<div class="sectionContent heart">
<h3><img src="images/health/sh_heart.gif" alt="ハートに優しい果実。" /></h3>
<div class="content">
<p>心臓や筋肉の正常な収縮を助けるカリウムが含まれます。ビタミンCとの相乗効果で血管を強化する働きも発揮し、心臓を健康に保つのに役立ちます。※アメリカン・ハート・アソシエイション(心臓病関連の任意団体)認定食材です。</p>
