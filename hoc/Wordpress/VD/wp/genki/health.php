<div class="sectionContent">
<h3><img src="images/health/sh_age.gif" alt="老いに負けないで。" /></h3>
<div class="content">
<p>老化の予防に、食物繊維であるペクチンの抗酸化作用や抗炎症作用が役立ちます。ペクチンは皮や袋、皮の裏側の白いふわふわの部 分に多く含まれています。</p>
